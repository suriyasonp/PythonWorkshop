tupleEx = (1,200,3,4,10)
listEx = (1,200,3,4,10)

print(listEx)
len(tupleEx)

'''Ex.1'''
print(len(tupleEx))
'''ฟังก์ชั่น len() ใช้แสดงจำนวนสมาชิกของ list,tuple,string,collection ต่างๆ '''

'''Ex.2'''
print(max(tupleEx))
'''ฟังก์ชั่น max() แสดงข้อมูลที่มากที่สุด'''

'''Ex.3'''
print(tuple(listEx))
'''ฟังก์ชั่น tuple() เปลี่ยนข้อมูล list เป็น tuple '''

'''Ex.4'''
print(tupleEx.index(200))
'''ฟังก์ชั่น index() ใช้สำแสดงตำแหน่งของข้อมูล'''
